package be.kuleuven.researchpad.domain;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.Table;

@Entity
@Table
public class RatingItem extends Base {
	@Id
	@GeneratedValue(strategy = GenerationType.AUTO)
	@Column(name = "id")
	private int id;
	
	@ManyToOne
	@JoinColumn(name="pdId")
	private PersonDocument personDocument;
	
	@Column
	private String content;

	public RatingItem() {
		super();
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public PersonDocument getPersonDocument() {
		return personDocument;
	}

	public void setPersonDocument(PersonDocument personDocument) {
		this.personDocument = personDocument;
	}

}
